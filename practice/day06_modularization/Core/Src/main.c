/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

	
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM1_Init();
  /* USER CODE BEGIN 2 */
HAL_TIM_Base_Start_IT(&htim1);//启动TIM1基础定时器中断

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
		if(timer_flag)//如果定时器中断到了以后，就会把timer_flag置为1
		{
			timer_flag=0;//先清零标志位，表示本次定时器事件已经处理
			g_sensor_data.tick++;//节拍计数加一
			LED_ALL_Toogle();//翻转LED状态
		}
		if(Key1_IsPressed())//如果检测KEY1被按下
		{
			g_sensor_data.mode=!g_sensor_data.mode;//切换模式
			HAL_TIM_Base_Stop_IT(&htim1);//修改定时器ARR前，先关闭定时器中断
			__HAL_TIM_SET_AUTORELOAD(&htim1,tim_arr[g_sensor_data.mode]);//根据mode选择不同的ARR值，从而改变LED的闪烁速度
			__HAL_TIM_SET_COUNTER(&htim1,0);//把计数器清零，避免旧计数值影响新的定时周期
			HAL_TIM_Base_Start_IT(&htim1);//重新启动定时器中断
			while(Key1_IsPressed());//等待按键松开，防止下一次按下被重复识别很多次
		}
		
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

